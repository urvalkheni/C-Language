// Sum of 2 Numbers //
#include<stdio.h>
int main(){
    int x;
    int y;
    int sum;
    printf("Please Enter First Digit :: ");
    scanf("%d",&x);
    printf("Please Enter Second Digit :: ");
    scanf("%d",&y);
    sum = x + y;
    printf("The Sum of number x & y is :: '%d' ",sum);
    return 0;
}