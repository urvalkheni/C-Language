#include <stdio.h>

int main() {
    printf("================ Linux Terminal Commands =================\n\n");
    printf("1. clear      > Clears the screen.\n");
    printf("2. date       > Shows current date and time.\n");
    printf("3. uname -a   > Displays system info.\n");
    printf("4. df -h      > Disk usage (human readable).\n");
    printf("5. ls -l      > Lists directory in long format.\n");
    printf("6. mkdir      > Creates a directory.\n");
    printf("7. rmdir      > Removes empty directory.\n");
    printf("8. touch      > Creates empty file.\n");
    printf("9. cat        > Displays file contents.\n");
    printf("10. cp        > Copies file.\n");
    printf("11. mv        > Moves/renames file.\n");
    printf("12. rm        > Deletes file.\n");
    printf("13. chmod     > Changes file permissions.\n");
    printf("14. chown     > Changes file owner.\n");
    printf("15. whoami    > Displays current user.\n");
    printf("16. top       > Shows running processes.\n");
    printf("17. ps aux    > Lists all processes.\n");
    printf("18. man       > Manual for commands.\n");
    printf("19. history   > Lists command history.\n");
    printf("20. exit      > Exits shell.\n");
    printf("21. grep      > Searches for text in files.\n");
    printf("22. echo      > Prints text to terminal.\n");
    printf("23. alias     > Creates command shortcuts.\n");
    printf("24. pwd       > Shows current working directory.\n");
    printf("25. find      > Finds files by name.\n");
    return 0;
}